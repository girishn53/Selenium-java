package stringPractice;

public class StringConcat {

	public static void main(String[] args) {

		//By using + operator
		String s = "Girish";

		String s1 = "Narayan";

		String s2 = s + s1;

		System.out.println(s2);
		
		
		//By using concat method
		
		
		String s7="Kunal";
		
		
		String s8=s7.concat("khemu");
		
		
		System.out.println(s8);
		
		}

}
